var materialColours: [MaterialColours: [UIColor]] = [:]
func getMaterialColourGroups() -> [MaterialColours: [UIColor]] {
    if (materialColours.count == 0) {
        for (group, colours) in materialColoursRaw {
            materialColours[group] = colours.map({
                (colour: [CGFloat]) -> UIColor in
                return UIColor(red: colour[0] / 255, green: colour[1] / 255, blue: colour[2] / 255, alpha: 1.0)
            })
        }
    }
    
    return materialColours
}

